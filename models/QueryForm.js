import { models, model, Schema } from "mongoose";

const QueryFormSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export default models.QueryForm || model("QueryForm", QueryFormSchema);
